#include"Exception.h"
#include<stdio.h>
#include<stdlib.h>
#include<string.h>


////////////////////////////////////////////
////////////////////////////////////////////


CException::CException(int code, char*msg)
{
	this->code = code;
	this->message = _strdup(msg);
}

////////////////////////////////////////////
////////////////////////////////////////////


CException::~CException()
{
	this->message = NULL;
	free(this->message);
	code = NULL;
}

////////////////////////////////////////////
////////////////////////////////////////////


void CException::PrintErrMessage(CException*e)
{
	printf("Eroarea %d ", e->code);
	printf("%s\n", e->message);
}

